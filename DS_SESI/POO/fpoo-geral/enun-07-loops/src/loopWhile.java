
public class loopWhile {

	public void loopWhileContador() {

		int contador = 11;
		
		while (contador <=10) {
			
			System.out.println("o valor do contador do loop while é " + contador);
			//contador = contador +1;
			//contador += 1;
			contador ++;

		}
		
	}
 
	public void loopWhileContador2(int contador) {

		while (contador <=10) {
			
			System.out.println("o valor do contador é " + contador);
	
			contador ++;

		}
		
	}
	
}
